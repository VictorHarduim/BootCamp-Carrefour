package src.aula1;

public class Calculadora {

    public static void soma(double n1, double n2) {
        double result = n1 + n2;
        System.out.println("A soma é: " + result);
    }
    public static void subtracao(double n1, double n2) {
        double result = n1 - n2;
        System.out.println("A diferença é: " + result);
    }
    public static void produto(double n1, double n2) {
        double result = n1 * n2;
        System.out.println("A multiplicação é: " + result);
    }
    public static void divisao(double n1, double n2) {
        double result = n1 / n2;
        System.out.println("A divisão é: " + result);
    }



}
